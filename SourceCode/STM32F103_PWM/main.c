void screen_receive()
{
  HAL_GPIO_TogglePin(GPIOB, GPIO_PIN_0);
  uint8_t led, frequency;
  for (int i = 0; i < 7; i++)
  {
    uart_screen_data[i] = read1BFromRingBuff(i);
  }

  switch (uart_screen_data[2])
  {
  case 0:
    frequency = 0;
    break;
  case 1:
    frequency = 1;
    break;
  case 2:
    frequency = 2;
    break;
  case 3:
    frequency = 3;
    break;
  default:
    TJCPrintf("Cannot Fine Frequency\n");
  }
  switch (uart_screen_data[1])
  {
  case 0x70:
    led = 0;
    // htim1.Instance->PSC = led_frequency_prescaler[frequency] - 1;
    break;
  case 0x71:
    led = 1;
    // htim2.Instance->PSC = led_frequency_prescaler[frequency] - 1;
    break;
  case 0x72:
    led = 2;
    // htim3.Instance->PSC = led_frequency_prescaler[frequency] - 1;
    break;
  case 0x73:
    led = 3;
    // htim4.Instance->PSC = led_frequency_prescaler[frequency] - 1;
    break;
  default:
    TJCPrintf("Cannot Find LED\n");
  }

  // é»˜è®¤4ä¸ªé¢‘çŽ?
  frequency = led * 1000 + frequency * led_frequency_delta[3];
  led_code[led] = frequency;

  TJCPrintf("n0.val=%d", led_code[0]);
  TJCPrintf("n1.val=%d", led_code[1]);
  TJCPrintf("n2.val=%d", led_code[2]);
  TJCPrintf("n3.val=%d\n", led_code[3]);

  for (int i = 0; i < 7; i++)
    TJCPrintf("d%d = %d", i, uart_screen_data[i]);
  TJCPrintf("length = %d", getRingBuffLenght());

  TJCPrintf("ok");
  initRingBuff();
}

